package OCT.ex_11102024_operators;

public class Lab011_concatenation
{
    public static void main(String[] args)
    {
       int a=10, b=30;

        System.out.println(a+b);

        String Name1 = "Nischitha";
        String Name2 = "Joshi";

        System.out.println(Name1+Name2);

        char si='a';
        char is='b';

        System.out.println(si+is);

        /* +  Rules of concatenation operator behave differently based on the data type
          int it will add mathematics
         in string it will act as the concatenation
          in Char it will add the ASCII value
         */


    }
}
